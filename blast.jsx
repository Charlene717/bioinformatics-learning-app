function BlastAlignment({ query, subject, hit, dark }){
  const fg = dark?'#F0EEE5':'#0F1614';
  const muted = dark?'#9E9C90':'#707974';
  const line = dark?'#2A2D29':'#E5E2D9';
  const bg = dark?'#14160E':'#F6F4EC';

  // chunk into 30-char rows for readability
  const ROW = 30;
  const rows = [];
  for(let i=0;i<query.length;i+=ROW){
    rows.push({
      q: query.slice(i, i+ROW),
      s: subject.slice(i, i+ROW),
      start: i+1,
    });
  }

  return (
    <div style={{
      padding:'12px 14px 14px', background:bg,
      borderTop:`1px dashed ${line}`,
    }}>
      {rows.map((r, ri)=>{
        const matchLine = r.q.split('').map((b,i)=>{
          const sb = r.s[i];
          if(sb==='-' || b==='-') return ' ';
          return b===sb ? '|' : ' ';
        }).join('');
        return (
          <div key={ri} style={{
            fontFamily:"'JetBrains Mono',monospace", fontSize:11.5,
            lineHeight:1.5, marginBottom: ri<rows.length-1?8:0,
          }}>
            <div style={{ display:'flex', gap:8 }}>
              <span style={{ color:muted, width:32, textAlign:'right' }}>Q {String(r.start).padStart(2,' ')}</span>
              <span>
                {r.q.split('').map((b,i)=>(
                  <span key={i} style={{ color: r.s[i]===b ? fg : '#D9594C', fontWeight: r.s[i]===b ? 500 : 700 }}>{b}</span>
                ))}
              </span>
            </div>
            <div style={{ display:'flex', gap:8 }}>
              <span style={{ width:32 }}/>
              <span style={{ color:'var(--accent)', letterSpacing:.5 }}>{matchLine}</span>
            </div>
            <div style={{ display:'flex', gap:8 }}>
              <span style={{ color:muted, width:32, textAlign:'right' }}>S {String(r.start).padStart(2,' ')}</span>
              <span>
                {r.s.split('').map((b,i)=>(
                  <span key={i} style={{
                    color: b==='-' ? muted : r.q[i]===b ? fg : '#D9594C',
                    fontWeight: r.q[i]===b ? 500 : 700,
                  }}>{b}</span>
                ))}
              </span>
            </div>
          </div>
        );
      })}

      {/* footer stats */}
      <div style={{
        display:'flex', justifyContent:'space-between', marginTop:10, paddingTop:10,
        borderTop:`1px dashed ${line}`,
        fontFamily:"'JetBrains Mono',monospace", fontSize:10.5, color:muted, letterSpacing:.3,
      }}>
        <span>Identities <b style={{ color:fg }}>{hit.ident}/60 ({Math.round(hit.ident/60*100)}%)</b></span>
        <span>Gaps <b style={{ color:fg }}>{(hit.gaps?.length||0)}/60</b></span>
      </div>
    </div>
  );
}

