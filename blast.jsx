// Make subject sequence from query + mut list (mismatches) + gap positions (deletions in subject)
function makeSubject(query, muts=[], gaps=[]){
  const mset = new Set(muts), gset = new Set(gaps);
  const alt = { A:'T', T:'A', G:'C', C:'G', M:'A' };
  let out = '';
  for(let i=0;i<query.length;i++){
    if(gset.has(i)) out += '-';
    else if(mset.has(i)) out += (alt[query[i]] || 'N');
    else out += query[i];
  }
  return out;
}

// ─────────── BlastTool ───────────
function BlastTool({ dark=false }){
  const [queryKey, setQueryKey] = React.useState('HBB');
  const q = BLAST_QUERIES[queryKey];
  const hits = BLAST_HITS[queryKey];

  const [phase, setPhase] = React.useState('idle'); // idle | scan | done
  const [scanProg, setScanProg] = React.useState(0);
  const [visibleHits, setVisibleHits] = React.useState(0);
  const [expanded, setExpanded] = React.useState(0); // index of expanded hit

  // run the animation
  const run = ()=>{
    setPhase('scan');
    setScanProg(0);
    setVisibleHits(0);
    setExpanded(0);
  };

  React.useEffect(()=>{
    if(phase!=='scan') return;
    if(scanProg>=100){
      setPhase('done');
      return;
    }
    const t = setTimeout(()=>setScanProg(p=>Math.min(100, p+4)), 50);
    return ()=>clearTimeout(t);
  }, [phase, scanProg]);

  React.useEffect(()=>{
    if(phase!=='done') return;
    if(visibleHits>=hits.length) return;
    const t = setTimeout(()=>setVisibleHits(v=>v+1), 90);
    return ()=>clearTimeout(t);
  }, [phase, visibleHits, hits.length]);

  // reset when query changes
  React.useEffect(()=>{
    setPhase('idle'); setScanProg(0); setVisibleHits(0); setExpanded(0);
  }, [queryKey]);

  const fg = dark?'#F0EEE5':'#0F1614';
  const muted = dark?'#9E9C90':'#707974';
  const surf = dark?'#1E211D':'#fff';
  const line = dark?'#2A2D29':'#E5E2D9';

  return (
    <div style={{ display:'flex', flexDirection:'column', gap:14 }}>
      {/* Query selector */}
      <div style={{ background:surf, borderRadius:18, padding:14, border:`1px solid ${line}` }}>
        <div style={{ display:'flex', alignItems:'center', justifyContent:'space-between', marginBottom:10 }}>
          <div style={{ fontFamily:'Space Grotesk', fontWeight:600, fontSize:14, color:fg }}>Query</div>
          <div style={{ fontFamily:"'JetBrains Mono',monospace", fontSize:10, color:muted, letterSpacing:.6 }}>
            nucleotide · 60 nt
          </div>
        </div>

        {/* preset chips */}
        <div style={{ display:'flex', gap:6, flexWrap:'wrap', marginBottom:10 }}>
          {Object.keys(BLAST_QUERIES).map(k=>{
            const on = k===queryKey;
            return (
              <button key={k} onClick={()=>setQueryKey(k)} style={{
                padding:'6px 10px', borderRadius:99,
                border:`1px solid ${on?'var(--accent)':line}`,
                background: on?'var(--accent-soft)':dark?'#14160E':'#F6F4EC',
                color: on?'var(--accent-ink)':fg,
                fontSize:11, fontWeight:600, fontFamily:'Manrope, Noto Sans TC',
                cursor:'pointer',
              }}>{BLAST_QUERIES[k].label}</button>
            );
          })}
        </div>

        {/* seq display */}
        <div style={{
          background: dark?'#14160E':'#F6F4EC', borderRadius:10,
          padding:'10px 12px', border:`1px dashed ${line}`,
        }}>
          <div style={{
            fontFamily:"'JetBrains Mono',monospace", fontSize:11.5, color:fg,
            letterSpacing:.8, lineHeight:1.5, wordBreak:'break-all',
          }}>
            {q.seq.split('').map((b,i)=>(
              <span key={i} style={{ color: BASE_COLORS[b] || fg }}>{b}</span>
            ))}
          </div>
          <div style={{ fontSize:10, color:muted, marginTop:6, fontFamily:'Noto Sans TC' }}>{q.desc}</div>
        </div>

        <button onClick={run}
          disabled={phase==='scan'}
          style={{
            width:'100%', marginTop:12, padding:'11px 14px', borderRadius:12, border:'none',
            background: phase==='scan'?'#9E9C90':'var(--accent)', color:'#fff',
            fontWeight:600, fontSize:14, fontFamily:'Manrope, system-ui',
            cursor: phase==='scan'?'default':'pointer',
            display:'flex', alignItems:'center', justifyContent:'center', gap:8,
          }}>
          <svg width="14" height="14" viewBox="0 0 22 22" fill="none">
            <circle cx="9" cy="9" r="6" stroke="#fff" strokeWidth="2"/>
            <path d="M14 14l5 5" stroke="#fff" strokeWidth="2" strokeLinecap="round"/>
          </svg>
          {phase==='idle' && '執行 BLAST 搜尋'}
          {phase==='scan' && '掃描資料庫中⋯'}
          {phase==='done' && '重新搜尋'}
        </button>
      </div>

      {/* Scanning animation */}
      {phase==='scan' && (
        <ScanAnimation prog={scanProg} dark={dark}/>
      )}

      {/* Results */}
      {phase==='done' && (
        <div>
          <div style={{
            display:'flex', justifyContent:'space-between', alignItems:'center',
            padding:'0 4px 10px',
            fontFamily:"'JetBrains Mono',monospace", fontSize:11, color:muted, letterSpacing:.5,
          }}>
            <span>BLAST RESULTS</span>
            <span><b style={{ color:fg }}>{hits.length}</b> hits · sorted by E-value</span>
          </div>

          {/* color legend */}
          <div style={{
            background:surf, borderRadius:14, padding:'10px 12px',
            border:`1px solid ${line}`, marginBottom:10,
            display:'flex', alignItems:'center', gap:10,
          }}>
            <div style={{ fontFamily:"'JetBrains Mono',monospace", fontSize:10, color:muted, letterSpacing:.6 }}>BITS</div>
            <div style={{ flex:1, display:'flex', height:8, borderRadius:99, overflow:'hidden' }}>
              <div style={{ flex:1, background:'#9C77C7' }}/>
              <div style={{ flex:1, background:'#D9594C' }}/>
              <div style={{ flex:1, background:'#EAA532' }}/>
              <div style={{ flex:1, background:'#0E9384' }}/>
            </div>
            <div style={{ fontFamily:"'JetBrains Mono',monospace", fontSize:10, color:muted }}>&lt;50 — 50–80 — 80–200 — &gt;200</div>
          </div>

          {hits.slice(0, visibleHits).map((h, i)=>(
            <BlastHitCard key={h.acc}
              hit={h} query={q.seq}
              isTop={i===0}
              expanded={expanded===i}
              onToggle={()=>setExpanded(expanded===i?-1:i)}
              dark={dark}
            />
          ))}
        </div>
      )}
    </div>
  );
}

function ScanAnimation({ prog, dark }){
  const fg = dark?'#F0EEE5':'#0F1614';
  const muted = dark?'#9E9C90':'#707974';
  const surf = dark?'#1E211D':'#fff';
  const line = dark?'#2A2D29':'#E5E2D9';

  // simulated candidate sequences flying past
  const candidates = React.useMemo(()=>{
    const arr=[];
    for(let i=0;i<22;i++){
      const acc = ['NM_','XM_','NR_','XR_','EU','AY','KX'][i%7] + String(Math.floor(Math.random()*999999)).padStart(6,'0');
      const bits = Math.round(Math.random()*250 + 30);
      arr.push({ acc, bits });
    }
    return arr;
  }, []);

  const idxShown = Math.min(candidates.length, Math.floor(prog/100*candidates.length));

  return (
    <div style={{
      background:surf, borderRadius:18, padding:16, border:`1px solid ${line}`,
    }}>
      <div style={{
        display:'flex', justifyContent:'space-between', alignItems:'center', marginBottom:10,
        fontFamily:"'JetBrains Mono',monospace", fontSize:11, color:muted, letterSpacing:.5,
      }}>
        <span>SCANNING DATABASE</span>
        <span><b style={{ color:fg }}>{prog}%</b></span>
      </div>
      <ProgressBar pct={prog/100} color="var(--accent)" track={dark?'rgba(255,255,255,.08)':'rgba(0,0,0,.06)'} height={6}/>

      <div style={{
        marginTop:14, height:170, overflow:'hidden', position:'relative',
        background: dark?'#14160E':'#F6F4EC', borderRadius:10,
        border:`1px dashed ${line}`,
      }}>
        {/* scrolling candidates */}
        <div style={{
          position:'absolute', inset:'10px 12px',
          fontFamily:"'JetBrains Mono',monospace", fontSize:11,
          color:muted, lineHeight:1.6,
        }}>
          {candidates.slice(0, idxShown).map((c,i)=>{
            const opacity = Math.max(0.15, 1 - (idxShown - i - 1) * 0.08);
            const isCurrent = i===idxShown-1;
            return (
              <div key={i} style={{ display:'flex', justifyContent:'space-between', opacity, color: isCurrent ? fg : muted }}>
                <span>{isCurrent && '▸ '}{c.acc}</span>
                <span style={{ color: c.bits>200?'#0E9384':c.bits>80?'#EAA532':'#9C77C7' }}>bits {c.bits}</span>
              </div>
            );
          })}
        </div>
        {/* gradient fade */}
        <div style={{
          position:'absolute', inset:0, pointerEvents:'none',
          background: `linear-gradient(180deg, ${dark?'#14160E':'#F6F4EC'} 0%, transparent 25%, transparent 75%, ${dark?'#14160E':'#F6F4EC'} 100%)`,
        }}/>
      </div>

      <div style={{
        marginTop:10, fontSize:11, color:muted, fontFamily:'Noto Sans TC',
      }}>
        對齊 query 與資料庫中每一筆 subject，計算 score、E-value 並排序中⋯
      </div>
    </div>
  );
}

